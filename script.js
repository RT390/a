let currentQuote = "";

const quoteEl = document.getElementById("quoteText");
const copyBtn = document.getElementById("copyBtn");
const shareBtn = document.getElementById("shareBtn");
const randomBtn = document.getElementById("randomBtn");
const counterSpan = document.getElementById("quoteCounter");
const toast = document.getElementById("toast");

function showToast(message) {
    toast.textContent = message;
    toast.classList.add("show");
    setTimeout(() => {
        toast.classList.remove("show");
    }, 2000);
}

function displayRandomQuote() {
    const randomIndex = Math.floor(Math.random() * quotesList.length);
    currentQuote = quotesList[randomIndex];
    quoteEl.textContent = currentQuote;
    if (counterSpan) {
        counterSpan.textContent = `📜 حكمة ${randomIndex+1} من ${quotesList.length}`;
    }
}

// نسخ النص
async function copyQuote() {
    if (!currentQuote) return;
    try {
        await navigator.clipboard.writeText(currentQuote);
        showToast("✅ تم نسخ الحكمة!");
    } catch (err) {
        showToast("❌ فشل النسخ، حاول يدوياً");
    }
}

// مشاركة عبر Web Share API أو نسخ الرابط
function shareQuote() {
    if (!currentQuote) return;
    const shareData = {
        title: "جيب الحكم",
        text: currentQuote,
        url: window.location.href,
    };
    if (navigator.share) {
        navigator.share(shareData).catch((e) => {
            if (e.name !== "AbortError") showToast("⚠️ لم نتمكن من المشاركة");
        });
    } else {
        // بديل: نسخ النص مع رسالة
        navigator.clipboard.writeText(currentQuote + "\n\n— من موقع جيب الحكم");
        showToast("📋 تم نسخ الحكمة للمشاركة");
    }
}

// الأحداث
randomBtn.addEventListener("click", displayRandomQuote);
copyBtn.addEventListener("click", copyQuote);
shareBtn.addEventListener("click", shareQuote);

